const opportunity = (s: Niche["scores"]) =>
  Math.round(
    (100 - s.competition) * 0.25 +
      s.virality * 0.25 +
      s.monetization * 0.2 +
      s.ease * 0.15 +
      s.repeatability * 0.15
  );

