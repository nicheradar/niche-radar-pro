  style: "faceless" | "personal" | "both";
  beginnerFriendly: boolean;
  region: "global" | "us" | "eu" | "asia" | "latam";
  scores: {
    competition: number; // lower = better, 0-100 (we'll display inversely)
    virality: number;
    monetization: number;
    ease: number;
    repeatability: number;
  };
